# Kurs Dolar ke Rupiah
kurs_dolar_ke_rupiah = 15102.10

# Input jumlah dolar
jumlah_dolar = float(input("Masukkan jumlah dolar yang ingin diubah: "))

# Menghitung jumlah rupiah
jumlah_rupiah = jumlah_dolar * kurs_dolar_ke_rupiah

# Menampilkan hasil
print(f"{jumlah_dolar} Dolar sama dengan {jumlah_rupiah:.2f} Rupiah.")
